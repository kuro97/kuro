"""Утилита нормализации телефонных номеров."""

import re


def normalize_phone(raw: str | None) -> str:
    """Возвращает последние 10 цифр телефона.

    Примеры:
        '+77004982670' -> '7004982670'
        '8 700 498 26 70' -> '7004982670'
        '7004982670' -> '7004982670'
        '100' -> '100'          # короткие номера (extensions) возвращаем как есть (только цифры)
        None -> ''
        '' -> ''

    Логика:
        1) Если raw пустой/None -> ''.
        2) Оставляем только цифры.
        3) Если получилось >= 10 цифр -> берём последние 10.
        4) Иначе -> возвращаем что есть (короткие extensions, тестовые значения).
    """
    # Пустое или None — возвращаем пустую строку
    if not raw:
        return ""

    # Оставляем только цифры
    digits = re.sub(r"\D", "", raw)

    # Пустой результат после очистки
    if not digits:
        return ""

    # Если 10+ цифр — берём последние 10 (стандартный формат без кода страны)
    if len(digits) >= 10:
        return digits[-10:]

    # Короткий extension — возвращаем как есть
    return digits
